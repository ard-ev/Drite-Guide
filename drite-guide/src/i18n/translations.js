function deepMerge(base, override) {
  if (!override) return base;

  return Object.keys(override).reduce(
    (merged, key) => {
      const baseValue = merged[key];
      const overrideValue = override[key];

      if (
        baseValue &&
        overrideValue &&
        typeof baseValue === 'object' &&
        typeof overrideValue === 'object' &&
        !Array.isArray(baseValue) &&
        !Array.isArray(overrideValue)
      ) {
        merged[key] = deepMerge(baseValue, overrideValue);
      } else {
        merged[key] = overrideValue;
      }

      return merged;
    },
    { ...base }
  );
}

export const dictionaries = SUPPORTED_LANGUAGE_CODES.reduce((acc, languageCode) => {
  acc[languageCode] = deepMerge(translations.en, overrides[languageCode]);
  return acc;
}, {});

function resolvePath(source, path) {
  return String(path)
    .split('.')
    .reduce((value, part) => (value == null ? undefined : value[part]), source);
}

function interpolate(template, params = {}) {
  if (typeof template !== 'string') {
    return template;
  }

  return template.replace(/\{\{(\w+)\}\}/g, (match, key) =>
    params[key] == null ? match : String(params[key])
  );
}

export function normalizeLanguageCode(languageCode) {
  return SUPPORTED_LANGUAGE_CODES.includes(languageCode) ? languageCode : 'en';
}

export function translate(languageCode, key, params) {
  const language = normalizeLanguageCode(languageCode);
  const value =
    resolvePath(dictionaries[language], key) ?? resolvePath(dictionaries.en, key);

  if (value == null) {
    return key;
  }

  return interpolate(value, params);
}

export function translateCount(languageCode, key, count, params = {}) {
  const countKey = count === 1 ? `${key}.one` : `${key}.other`;
  return translate(languageCode, countKey, { count, ...params });
}
